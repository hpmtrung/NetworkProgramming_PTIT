    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String msg = txaMessage.getText().trim();

        if (msg.isEmpty()) {
            return;
        }

        txaChat.append("\nTôi: " + msg);
        DefaultCaret caret = (DefaultCaret) txaChat.getCaret();
        caret.setUpdatePolicy(DefaultCaret.ALWAYS_UPDATE);

        try {
            dos.writeUTF(msg + "\n");
        } catch (IOException ex) {
            System.err.println("Error while sendding messeger in " + this.name);
        }

        txaMessage.setText("");
    }//GEN-LAST:event_btnSendActionPerformed

    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        try {
            //Lấy dữ liệu bao gồm name,ip,port
            String ip = txfIP.getText().trim();
            int port = Integer.parseInt(txfPort.getText().trim());
            String name = txfName.getText().trim();
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập tên");
                return;
            }
            this.name = name;
            //Tạo một socket bằng ip và port ở trên
            client = new Socket(ip, port);

            if (client == null) {
                throw new NullPointerException("Null client socket instance");
            }

            dis = new DataInputStream(client.getInputStream());
            dos = new DataOutputStream(client.getOutputStream());

            // Ghi danh tên client
            dos.writeUTF(this.name);

            txaChat.setText("");

            //Cho ChatPanel này "chạy" để kiểm tra tin nhắn đến và đi
            Thread t = new Thread(this);
            t.start();

            //Thông báo chạy thành công
            JOptionPane.showMessageDialog(this, "Kết nối thành công", "",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Xảy ra lỗi khi kết nối. Vui lòng thử lại!\nChi tiết lỗi: " + e.getMessage(),
                    "Lỗi kết nối", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConnectActionPerformed

    @Override
    public void run() {
        while (true) {
            try {
                if (client != null) {
                    String msg = "";
                    while ((msg = dis.readUTF()) != null) {
                        //Nếu có tin nhắn đến thì ghi vào lịch sử
                        txaChat.append("\n" + msg);
                    }
                }
            } catch (Exception e) {
            }
        }
    }

